export function SimpleEditor({
  onChange,
  onIssues,
}: {
  onChange: (content: string) => void;
  onIssues: Dispatch<SetStateAction<string[]>>;
}) {
  const isMobile = useMobile();
  const windowSize = useWindowSize();
  const editor = useEditor({
    extensions: [
      StarterKit,
      Image,
      TaskItem,
      TaskList,
      TextAlign,
      Typography,
      Highlight,
      Subscript,
      Superscript,
      Underline,
      Link,
      Selection,
      TrailingNode,
      ImageUploadNode,
    ],
    content,
    onUpdate: ({ editor }) => {
      onChange(editor.getHTML());
    },
  });
  const [mobileView, setMobileView] = React.useState<
    "main" | "highlighter" | "link"
  >("main");
  const toolbarRef = React.useRef<HTMLDivElement>(null);

  const [loading, setLoading] = React.useState(false);

  // Animated dots component
  const LoadingDots = () => {
    const [dots, setDots] = React.useState("");
    React.useEffect(() => {
      if (!loading) {
        setDots("");
        return;
      }
      const interval = setInterval(() => {
        setDots((prev) => (prev.length < 3 ? prev + "." : ""));
      }, 500);
      return () => clearInterval(interval);
    }, [loading]);
    return <span style={{ marginLeft: 5 }}>{dots}</span>;
  };

  const handleDialogSubmit = async () => {
    if (!editor) return;
    setLoading(true);
    try {
      const text = editor.getText();
      console.log("Sending editor content:", text);
      const res = await fetch("http://localhost:8000/api/chat", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ text, question: '', in_text: true }),
      });
      const data = await res.json();
      onIssues(data.response.lines || []);
      console.log("Received issues:", data.response.lines);
    } catch (error) {
      console.log("Error fetching chat response:", error);
    } finally {
      setLoading(false);
    }
  };

  // ...rest of your code unchanged

  return (
    <EditorContext.Provider value={{ editor }}>
      <Toolbar
        ref={toolbarRef}
        style={{
          background:
            "linear-gradient(135deg, #001f3f 0%, #003366 50%, #004080 100%)",
          color: "white",
          minHeight: isMobile ? "100vh" : undefined,
          display: isMobile ? "flex" : undefined,
          flexDirection: isMobile ? "column" : undefined,
        }}
        className="custom-toolbar"
      >
        <Button
          className="tiptap-dialog-button"
          data-style="primary"
          onClick={handleDialogSubmit}
          style={{ color: "white" }}
          disabled={loading}
        >
          Analyze Content
          {loading && <LoadingDots />}
        </Button>

        {mobileView === "main" ? (
          <MainToolbarContent
            onHighlighterClick={() => setMobileView("highlighter")}
            onLinkClick={() => setMobileView("link")}
            isMobile={isMobile}
          />
        ) : (
          <MobileToolbarContent
            type={mobileView === "highlighter" ? "highlighter" : "link"}
            onBack={() => setMobileView("main")}
          />
        )}
      </Toolbar>

      <div className="content-wrapper">
        <EditorContent
          editor={editor}
          role="presentation"
          className="simple-editor-content"
        />
      </div>
    </EditorContext.Provider>
  );
}
